// 시/군 이름 추출 (RDA 관측소명에서 사용)
// "춘천시" -> "춘천", "홍천군" -> "홍천"
export const extractCityName = (fullName: string): string => {
  return fullName.replace(/시$|군$|구$/, '');
};

// 관측소 이름에서 시/군 찾기
export const findSigByStationName = (stationName: string): SigCentroid | null => {
  // 정확히 일치하는 경우
  if (sigCentroids[stationName]) {
    return sigCentroids[stationName];
  }

  // 시/군 이름으로 검색 (예: "춘천" -> "춘천시")
  for (const [name, coords] of Object.entries(sigCentroids)) {
    if (name.includes(stationName) || stationName.includes(extractCityName(name))) {
      return coords;
    }
  }

  return null;
};
